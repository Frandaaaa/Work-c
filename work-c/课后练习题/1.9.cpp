 #include <iostream>

 using namespace std;

 int main()
 {
     double area;
     double width = 4.5;
     double height = 7.9;

     area = width * height;

     cout<<"长宽分别为7.9和4.5的长方形的面积为"<<area<<endl;

     return 0;
 }
